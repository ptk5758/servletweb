package model;

public class Menu {
	private String name;
	private String href;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setHref(String href) {
		this.href = href;
	}
	
	public String getHref() {
		return href;
	}
	
}
